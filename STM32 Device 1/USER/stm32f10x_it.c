void TIM2_IRQHandler(void)
{

	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)
    {
        TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
        tick_1ms++;
				if(tick_1ms >= 10)
				{
					tick_1ms = 0;
					tick_5ms++;
					tick_200ms++;
					if(tick_200ms >= 200)
					{
						tick_200ms = 0;
					}
				}
					speed_count++;
					if(speed_count >= 50)
					{
						speed_count = 0;
					}	
					
					duoji_count++;
					if(duoji_count <= zhuanjiao)
					{
						Servo_SET;
					}
					else
					{
						Servo_RESET;
					}
					if(duoji_count >= 200)//20ms
					{
						duoji_count = 0;
					}
					}
}

